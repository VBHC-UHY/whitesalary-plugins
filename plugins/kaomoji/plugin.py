"""颜文字生成插件"""
import random

KAOMOJI = {
    "开心": ["(◕‿◕)", "(｡◕‿◕｡)", "(✿◠‿◠)", "٩(◕‿◕｡)۶", "(◠‿◠)", "(*^▽^*)", "(≧▽≦)", "ヽ(>∀<☆)ノ"],
    "难过": ["(╥﹏╥)", "(；′⌒`)", "(´;ω;`)", "(っ˘̩╭╮˘̩)っ", "( ´_ゝ`)", "(T_T)", "(;_;)"],
    "生气": ["(╯°□°)╯︵ ┻━┻", "(ノಠ益ಠ)ノ", "(`Д´)", "(≧Д≦)", "ヽ(`Д´)ノ"],
    "惊讶": ["(°o°)", "(O.O)", "Σ(°△°|||)", "(⊙_⊙)", "∑(゜Д゜;)"],
    "爱心": ["(♥ω♥*)", "(◍•ᴗ•◍)❤", "(´,,•ω•,,)♡", "♡(ŐωŐ人)", "(◕‿◕)♡"],
    "可爱": ["(◕ᴗ◕✿)", "(｡♥‿♥｡)", "(●'◡'●)", "ヾ(◍°∇°◍)ﾉﾞ", "(◠‿・)—☆"],
    "无语": ["(￣_￣|||)", "(−_−) zzZ", "(¬_¬)", "( ˘_˘ )", "┐(´д`)┌"]
}

def run(mood: str = None, **kwargs):
    """根据心情生成颜文字"""
    if mood and mood in KAOMOJI:
        kaomoji = random.choice(KAOMOJI[mood])
        return f"这是{mood}的颜文字：{kaomoji}"
    else:
        # 随机选择一个类别
        mood = random.choice(list(KAOMOJI.keys()))
        kaomoji = random.choice(KAOMOJI[mood])
        return f"随机颜文字（{mood}）：{kaomoji}\n\n可选心情：{', '.join(KAOMOJI.keys())}"

