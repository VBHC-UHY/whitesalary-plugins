"""彩虹屁生成插件 - 花式夸人"""
import random

RAINBOW_FARTS = [
    "你今天看起来像是从漫画里走出来的主角！✨",
    "你的存在让这个世界的平均颜值都提高了！🌟",
    "你笑起来的样子，比阳光还要温暖！☀️",
    "你简直是人间理想的样子！💫",
    "和你聊天是我今天最开心的事！😊",
    "你的才华就像星星一样闪闪发光！⭐",
    "你是我见过最有魅力的人！💕",
    "你的声音比最动听的音乐还要悦耳！🎵",
    "你就是传说中别人家的孩子吧！👏",
    "你的气质真的太出众了！🌈",
    "你简直是行走的正能量发电机！⚡",
    "你的眼睛里有星辰大海！🌊",
    "你的笑容可以治愈一切！😄",
    "你是这个世界上独一无二的宝藏！💎",
    "你的优秀让我词穷！🏆"
]

def run(target: str = "你", **kwargs):
    """生成彩虹屁"""
    fart = random.choice(RAINBOW_FARTS)
    if target and target != "我":
        fart = fart.replace("你", target)
    return f"🌈 {fart}"

